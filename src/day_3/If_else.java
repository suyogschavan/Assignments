package day_3;

public class If_else {

	public static void main(String[] args) {
		int i = 1;
		
		 
		if(i < 10) 
			System.out.println("i is less than 10 : "+i);
		
		
		i = 11;
		
		// if else statement 
		
		if(i < 10) 
			 System.out.println("i is less than 10 : "+i);
		
		else 
			 System.out.println("i is greater than 10 : "+i);
		
			
	}

}
